# Prompt the user to enter three integer values
value1 = int(input("Enter the first integer: "))
value2 = int(input("Enter the second integer: "))
value3 = int(input("Enter the third integer: "))

# Calculate the sum
sum_result = value1 + value2 + value3

# Display the result
print(f"The sum of {value1}, {value2}, and {value3} is: {sum_result}")
